// Clock myClock

// 类的成员函数
//     在类中声明函数原型；
//     可以在类外给出函数体实现，并在函数名前使用类名加以限定；
//     也可以直接在类中给出函数体，形成内联成员函数；
//     允许声明重载函数和带默认参数值的函数；


// 内联成员函数
//     为了提高运行时的效率，对于较简单的函数可以声明为内联形式。
//     内联函数体中不要有复杂结构
//     （如循环语句和switch语句）
//     在类中声明内联成员函数的方式：
//         将函数体放在类的声明中
//         使用inline关键字