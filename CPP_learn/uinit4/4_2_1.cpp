// class   类的名称
// {
//     public:
//             公有成员（外部接口）
//             任何外部函数都可以访问公有类型的数据和函数
//     private:
//             私有成员
//             只允许本类中的函数访问，类外部的任何函数不能访问
//             若紧跟在类名称后面声明私有成员，则关键字private可以省略
//     protected:
//             保护型成员
//             与private类似，其差别表现在继承与派生时派生类的影响不同
// }

class Clock{
    public:
        void setTime(int newH, int newM, int newS);
        void showTime();
    private:
        int hour = 0, minute = 0, second = 0;
};


